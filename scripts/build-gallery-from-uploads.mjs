/**
 * scripts/build-gallery-from-uploads.mjs
 * 
 * Generează automat src/data/gallery.ts din lista de imagini reale.
 * 
 * Utilizare:
 *   1. Extrage lista de imagini de pe server:
 *      ssh root@46.225.182.127 "find /srv/superparty/public/wp-content/uploads -type f \( -iname '*.jpg' -o -iname '*.png' -o -iname '*.webp' \) | grep -v '\-[0-9]\{2,4\}x[0-9]\{2,4\}\.' | sed 's|/srv/superparty/public||' | sort" > /tmp/uploads.txt
 *   
 *   2. Rulează scriptul:
 *      node scripts/build-gallery-from-uploads.mjs /tmp/uploads.txt
 *   
 *   3. Verifică src/data/gallery.generated.ts și copiază selectiv în gallery.ts
 */

import { readFileSync, writeFileSync } from 'fs';
import path from 'path';

const inputFile = process.argv[2] ?? '/tmp/uploads.txt';
const BASE = 'https://www.superparty.ro';

function categorize(filePath) {
    const p = filePath.toLowerCase();
    if (/ursitoare/.test(p)) return 'ursitoare';
    if (/vata|popcorn/.test(p)) return 'vata-popcorn';
    if (/balon|arcada|decor|heliu|confetti|piniata/.test(p)) return 'decor';
    if (/spiderman|batman|superman|elsa|olaf|mickey|minnie|ninja|printesa|cenusareasa|mascota|craciun|aladin|rapunzel|hulk|wonder/.test(p)) return 'personaje';
    if (/animatori|animator|copii|petreceri/.test(p)) return 'animatori';
    return 'animatori';
}

function toAlt(filePath) {
    const fn = path.basename(filePath, path.extname(filePath));
    return fn.replace(/-/g, ' ').replace(/_/g, ' ')
        .replace(/\b\w/g, c => c.toUpperCase())
        .slice(0, 60);
}

let lines;
try {
    lines = readFileSync(inputFile, 'utf8').split('\n').filter(l => l.trim());
} catch {
    console.error(`Cannot read ${inputFile}. Specify path to uploads list.`);
    process.exit(1);
}

// Filter: only /wp-content/uploads, no thumbnails
const paths = lines
    .filter(l => l.includes('/wp-content/uploads') && !/\-\d{2,4}x\d{2,4}\./.test(l))
    .filter(l => /\.(jpg|jpeg|png|webp)$/i.test(l));

const images = paths.map(p => ({
    url: `${BASE}${p}`,
    alt: toAlt(p),
    category: categorize(p),
}));

// Stats
const cats = {};
images.forEach(i => { cats[i.category] = (cats[i.category] ?? 0) + 1; });
console.log(`Found ${images.length} images:`, cats);

// Generate TS file
const lines_ts = [
    `// AUTO-GENERATED by scripts/build-gallery-from-uploads.mjs`,
    `// ${new Date().toISOString()}`,
    `import type { GalleryImage } from './gallery';`,
    ``,
    `export const galleryGenerated: GalleryImage[] = [`,
    ...images.map(i => `  { url: ${JSON.stringify(i.url)}, alt: ${JSON.stringify(i.alt)}, category: ${JSON.stringify(i.category)} },`),
    `];`,
].join('\n');

writeFileSync('src/data/gallery.generated.ts', lines_ts, 'utf8');
console.log(`Written: src/data/gallery.generated.ts`);
console.log(`Review and copy what you need into src/data/gallery.ts`);
